public class BlockTest {
    public static void main(String args[]){
        int x,Y;
        Y=20;

        //the target of this loop is block
        for(x=0; x<10; x++) {
            System.out.println("This is x: " + x);
            System.out.println("This is Y: " + Y);
            Y = Y - 2;

        }
    }
    
}
